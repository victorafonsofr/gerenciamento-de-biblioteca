    private void jbConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbConsultarActionPerformed
        try {
            DAO banco = new DAO();
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            String comando;
            if(jcbCategorias.getSelectedIndex()==0){
                comando = "SELECT titulo,autor,isbn,quantidade FROM livro";
            }else{
                comando = "SELECT titulo,autor,isbn,quantidade FROM livro where categoria_id='"+jcbCategorias.getSelectedIndex()+"';";
            }
            model.setNumRows(0);
            res = banco.consultar(comando);
            while(res.next()){
                model.addRow(new Object[]{
                    res.getString("titulo"),
                    res.getString("autor"),
                    res.getString("isbn"),
                    res.getString("quantidade"),
                });
            }
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(rootPane, "Erro ao consultar dados");
        }
        
    }//GEN-LAST:event_jbConsultarActionPerformed
    public void listarCategorias(){
            try {
                String comando = "Select * FROM categoria;";
                DAO banco = new DAO();
                res = banco.consultar(comando);
                while(res.next()){
                    jcbCategorias.addItem(res.getString("descricao"));
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao consultar");
            }
        
        }

